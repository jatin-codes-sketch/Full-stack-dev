//  setInterval
setInterval(()=>console.log("Hello"), 4000);

// setInterval is a method in JavaScript that repeatedly calls a function or executes a code snippet, with a fixed time delay between each call. It is commonly used for tasks such as updating the UI at regular intervals, polling a server for new data, or creating animations. The setInterval method takes two arguments: the function to be executed and the time interval (in milliseconds) between each execution. The function will continue to execute until it is stopped using clearInterval.


 